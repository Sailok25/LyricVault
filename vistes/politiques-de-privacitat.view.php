                <h2>Recopilació i Ús de les seves Dades Personals</h2>
                <h3>Tipus de Dades Recopilades</h3>
                <h4>Dades Personals</h4>
                <p>Mentre utilitza el nostre servei, podem demanar-li que ens proporcioni certa informació
                    d'identificació personal que pot ser utilitzada per contactar-lo o identificar-lo. La informació
                    d'identificació personal pot incloure, entre altres:</p>
                <ul>
                    <li>
                        <p>Adreça de correu electrònic</p>
                    </li>
                    <li>
                        <p>Nom i cognoms</p>
                    </li>
                    <li>
                        <p>Número de telèfon</p>
                    </li>
                    <li>
                        <p>Adreça, Estat, Província, Codi Postal/ZIP, Ciutat</p>
                    </li>
                    <li>
                        <p>Dades d'Ús</p>
                    </li>
                </ul>

                <h4>Dades d'Ús</h4>
                <p>Les Dades d'Ús es recopilen automàticament al fer servir el servei.</p>
                <p>Les Dades d'Ús poden incloure informació com l'adreça del Protocol d'Internet del seu dispositiu (per
                    exemple, adreça IP), tipus de navegador, versió del navegador, les pàgines del nostre servei que
                    visita, l'hora i la data de la seva visita, el temps que passa en aquestes pàgines, identificadors
                    únics de dispositius i altres dades de diagnòstic.</p>
                <p>Quan accedeix al servei a través d'un dispositiu mòbil, podem recopilar automàticament certa
                    informació, que inclou, entre altres, el tipus de dispositiu mòbil que utilitza, la seva ID única de
                    dispositiu mòbil, l'adreça IP del seu dispositiu mòbil, el seu sistema operatiu mòbil, el tipus de
                    navegador d'Internet mòbil que utilitza, identificadors únics de dispositius i altres dades de
                    diagnòstic.</p>
                <p>També podem recopilar informació que el seu navegador envia cada vegada que visita el nostre servei o
                    quan accedeix al servei a través d'un dispositiu mòbil.</p>

                <h4>Tecnologies de Seguiment i Cookies</h4>
                <p>Utilitzem cookies i tecnologies de seguiment similars per rastrejar l'activitat al nostre servei i
                    emmagatzemar certa informació. Les tecnologies de seguiment utilitzades són balises, etiquetes i
                    scripts per recopilar i rastrejar informació i per millorar i analitzar el nostre servei. Les
                    tecnologies que utilitzem poden incloure:</p>
                <ul>
                    <li><strong>Cookies o Cookies de Navegador.</strong> Una cookie és un petit fitxer que es col·loca
                        al seu dispositiu. Pot indicar al seu navegador que rebutgi totes les cookies o que li indiqui
                        quan s'envia una cookie. No obstant això, si no accepta cookies, és possible que no pugui
                        utilitzar algunes parts del nostre servei. A menys que hagi configurat el seu navegador per
                        rebutjar les cookies, el nostre servei pot utilitzar cookies.</li>
                    <li><strong>Balises Web.</strong> Certes seccions del nostre servei i els nostres correus
                        electrònics poden contenir petits fitxers electrònics coneguts com a balises web (també
                        anomenades gifs clars, etiquetes de píxels i gifs d'un sol píxel) que permeten a l'empresa, per
                        exemple, comptar els usuaris que han visitat aquestes pàgines o obert un correu electrònic i per
                        a altres estadístiques relacionades amb el lloc web (per exemple, registrar la popularitat d'una
                        secció determinada i verificar la integritat del sistema i del servidor).</li>
                </ul>
                <p>Les cookies poden ser "Persistents" o "De Sessió". Les cookies persistents romanen al seu ordinador
                    personal o dispositiu mòbil quan es desconnecta, mentre que les cookies de sessió s'eliminen tan
                    aviat com tanca el seu navegador web. Obtingui més informació sobre les cookies a l'article del <a
                        href="https://www.privacypolicies.com/blog/privacy-policy-template/#Use_Of_Cookies_Log_Files_And_Tracking"
                        target="_blank">lloc web de Polítiques de Privacitat</a>.</p>
                <p>Utilitzem tant cookies de sessió com persistents per als fins que es detallen a continuació:</p>
                <ul>
                    <li>
                        <p><strong>Cookies Necessàries / Essencials</strong></p>
                        <p>Tipus: Cookies de Sessió</p>
                        <p>Administrem: Necessàries per al funcionament del nostre servei.</p>
                    </li>
                    <li>
                        <p><strong>Cookies de Preferències</strong></p>
                        <p>Tipus: Cookies Persistents</p>
                        <p>Administrem: Utilitzem aquestes cookies per recordar les seves preferències i diverses
                            configuracions.</p>
