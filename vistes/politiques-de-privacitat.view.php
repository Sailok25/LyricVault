                <h2>Retenció de les seves Dades Personals</h2>
                <p>L'empresa retingués les seves dades personals només durant el temps que sigui necessari per als fins
                    establerts en aquesta Política de Privacitat. Retindrem i utilitzarem les seves dades personals en
                    la mesura necessària per complir les nostres obligacions legals (per exemple, si necessitem retenir
                    les seves dades per complir amb les lleis aplicables), resoldre disputes i fer complir les nostres
                    polítiques legals.</p>
                <p>L'empresa també retingués les dades d'ús durant fins d'anàlisi intern. Les dades d'ús generalment es
                    retindran durant un període més curt, excepte quan s'utilitzin per enfortir la seguretat o per
                    millorar la funcionalitat del nostre servei, o quan estiguem legalment obligats a retenir aquestes
                    dades durant períodes més llargs.</p>

                <h2>Transferència de les seves Dades Personals</h2>
                <p>La seva informació, incloent les dades personals, pot ser transferida a ordinadors ubicats fora del
                    seu estat, província, país o qualsevol altra jurisdicció governamental on les lleis de protecció de
                    dades poden diferir de les de la seva jurisdicció.</p>
                <p>Si es troba fora d'Espanya i decideix proporcionar-nos informació, tingui en compte que transferim
                    les dades, incloent les dades personals, a Espanya i les processsem allí.</p>
                <p>El seu consentiment a aquesta Política de Privacitat seguit de la seva enviament d'aquesta informació
                    representa el seu acord amb aquesta transferència.</p>
                <p>L'empresa prendrà totes les mesures raonablement necessàries per garantir que les seves dades es
                    tractin de manera segura i d'acord amb aquesta Política de Privacitat i que no es realitzarà cap
                    transferència de les seves dades personals a una organització o país a menys que hi hagi controls
                    adequats en el seu lloc, incloent la seguretat de les seves dades i altra informació personal.</p>

