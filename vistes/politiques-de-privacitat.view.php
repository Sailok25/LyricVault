                <h3>Transaccions Comercials</h3>
                <p>Si l'empresa participa en una fusió, adquisició o venda d'actius, les seves dades personals poden ser
                    transferides. Li notificarem abans que les seves dades personals siguin transferides i estiguin
                    subjectes a una Política de Privacitat diferent.</p>

                <h3>Requisits Legals</h3>
                <p>L'empresa pot divulgar les seves dades personals de la creença de bona fe que aquesta acció és
                    necessària per:</p>
                <ul>
                    <li>complir amb una obligació legal;</li>
                    <li>protegir i defensar els drets o la propietat de l'empresa;</li>
                    <li>prevenir o investigar possibles infraccions en connexió amb el servei;</li>
                    <li>protegir la seguretat personal dels usuaris del servei o del públic;</li>
                    <li>protegir-se contra la responsabilitat legal.</li>
                </ul>

                <h2>Seguretat de les seves Dades Personals</h2>
                <p>La seguretat de les seves dades personals és important per a nosaltres, però recordi que cap mètode
                    de transmissió per Internet o mètode d'emmagatzematge electrònic és 100% segur. Si bé ens esforcem
                    per utilitzar mitjans comercialment acceptables per protegir les seves dades personals, no podem
                    garantir la seva seguretat absoluta.</p>

                <h2>Els seus Drets de Protecció de Dades</h2>
                <p>Si és resident de l'Espai Econòmic Europeu (EEE), té certs drets de protecció de dades. L'empresa té
                    com a objectiu prendre mesures raonables per permetre-li corregir, modificar, eliminar o limitar
                    l'ús de les seves dades personals.</p>
                <p>Si desitja ser informat sobre quines dades personals tenim sobre vostè i si desitja que es treguin
                    del nostre sistema, contacti'ns.</p>
                <p>En certes circumstàncies, té els següents drets de protecció de dades:</p>
                <ul>
                    <li>El dret d'accés, actualitzar o eliminar la informació que tenim sobre vostè.</li>
                    <li>El dret a rectificar. Té dret a que es rectifiqui la seva informació si aquesta és inexacta o
                        incompleta.</li>
                    <li>El dret a objectar. Té dret a oposar-se al nostre processament de les seves dades personals.
                    </li>
                    <li>El dret a restringir. Té dret a sol·licitar que restrinjem el processament de la seva
                        informació.</li>
                    <li>El dret a la portabilitat de dades. Té dret a ser informat del seu dret a sol·licitar una còpia
                        de la informació que tenim sobre vostè en un format estructurat, llegible per màquina i d'ús
                        comú.</li>
                    <li>El dret a retirar el consentiment. Té dret a retirar el seu consentiment en qualsevol moment on
                        l'empresa es basés en el seu consentiment per processar la seva informació personal.</li>
                </ul>
                <p>Si us plau, tingueu en compte que és possible que li demanem que verifiqui la seva identitat abans de
                    respondre a aquestes sol·licituds.</p>
                <p>També té dret a presentar una queixa davant una autoritat de protecció de dades sobre la nostra
                    recopilació i ús de les seves dades personals. Per obtenir més informació, contacti amb l'autoritat
                    de protecció de dades local.</p>

